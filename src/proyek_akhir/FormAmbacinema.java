    private void updateComboJam() {
        cbJamTayang.removeAllItems();
        List<String> valid = logic.getJamTayangValid(masterJam);
        for (String j : valid) {
            cbJamTayang.addItem(j);
        }

        if (cbJamTayang.getItemCount() == 0) {
            cbJamTayang.addItem("Habis");
            bLnjtKePilKursi.setEnabled(false);
        }
    }

    // Fungsi untuk mendapatkan teks film yang dipilih
    private String getSelectedFilm() {
        if (ambacong.isSelected()) {
            return "Ambacong";
        }
        if (ambatar.isSelected()) {
            return "Ambatar";
        }
        if (argoNgawi.isSelected()) {
            return "Argo Ngawi Gejes Gejes";
        }
        return "";
    }

    private void refreshKursi() {
        String film = getSelectedFilm();
        String jam = cbJamTayang.getSelectedItem().toString();

        for (JToggleButton k : arrayKursi) {
            k.setSelected(false);
            if (logic.isKursiTerpesan(film, jam, k.getText())) {
                k.setEnabled(false);
            } else {
                k.setEnabled(true);
            }
        }
    }

