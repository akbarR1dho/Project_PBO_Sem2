    private void bLnjtKePilKursiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLnjtKePilKursiActionPerformed
        // TODO add your handling code here:
        if (getSelectedFilm().equals("")) {
            JOptionPane.showMessageDialog(this, "Pilih Film Terlebih Dahulu!");
            return;
        }
        refreshKursi();
        // Pindah Card ke Panel Kursi
        CardLayout cl = (CardLayout) panelInduk.getLayout();

        // Tampilkan halaman kursi berdasarkan "Card Name" yang sudah diatur tadi
        cl.show(panelInduk, "pilKursi");
    }//GEN-LAST:event_bLnjtKePilKursiActionPerformed

    private void bLnjtKeKonfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLnjtKeKonfirmasiActionPerformed
        // TODO add your handling code here:
        String film = getSelectedFilm();
        String jam = cbJamTayang.getSelectedItem().toString();
        List<String> dipilih = new ArrayList<>();

        for (JToggleButton k : arrayKursi) {
            if (k.isSelected() && k.isEnabled()) {
                dipilih.add(k.getText());
            }
        }

        if (dipilih.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih minimal 1 kursi!");
            return;
        }

        String kursiStr = String.join(", ", dipilih);

        // Fitur 5: Konfirmasi
        int tanya = JOptionPane.showConfirmDialog(this,
                "Konfirmasi Pesanan:\nFilm: " + film + "\nJam: " + jam + "\nKursi: " + kursiStr,
                "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (tanya == JOptionPane.YES_OPTION) {
            logic.pesanKursi(film, jam, dipilih);

            // Fitur 6: Struk
            String waktuPesan = LocalDateTime.now().format(DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm"));
            JOptionPane.showMessageDialog(this,
                    "======= STRUK =======\n"
                    + "Film: " + film + "\n"
                    + "Jam Tayang: " + jam + "\n"
                    + "Kursi: " + kursiStr + "\n"
                    + "Waktu Pesan: " + waktuPesan + "\n"
                    + "=====================");

            refreshKursi();
            bKbliKePilFilmActionPerformed(null); // Kembali ke menu utama
        }
    }//GEN-LAST:event_bLnjtKeKonfirmasiActionPerformed

